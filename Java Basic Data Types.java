class Solution {

    int javaIntType(Scanner sc) {
        int a = sc.nextInt();
        return a;
    }
    
    String javaStringType(Scanner sc) {
        String b = sc.next();
        return b;
    }
    
    float javaFloatType(Scanner sc) {
        Float c = sc.nextFloat();
        return c;
    }
};